    private void txt_phone_comKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_phone_comKeyReleased
        String s = txt_phone_com.getText();        
        char x = s.charAt(s.length() - 1);
        int a = x;
        if (a >= 97 && a <= 122 || a >= 65 && a <= 90) {
            txt_phone_com.setBackground(Color.red);
            add_button.setEnabled(false);
            
        } else {
            txt_phone_com.setBackground(Color.white);
            add_button.setEnabled(true);
//   txt_phone.setEditable(true);
        }           // TODO add your handling code here:
    }//GEN-LAST:event_txt_phone_comKeyReleased

