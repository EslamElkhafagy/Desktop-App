    private void add_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_buttonActionPerformed
        vendors_caps obj = new vendors_caps();
        vendors_cont ob = new vendors_cont();
        /*.........................................................*/
        String name = txt_name.getText();
        String code = txt_code.getText();
        String phone = txt_phone.getText();
        if (ob.add_validation().contains(name) || ob.add_validation().contains(code) || ob.add_validation().contains(phone)) {
            JOptionPane.showMessageDialog(null, " هذه البيانات موجودة سابقا  !!");
        } else if (txt_name.getText().equals("") || txt_phone.getText().equals("") || txt_address.getText().equals("") || txt_code.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Error in Insert please check data !!!  ");            
        } else {
            /*......................................................*/
            obj.setName(txt_name.getText());
            obj.setAddress(txt_address.getText());
            obj.setPhone(txt_phone.getText());
            obj.setCode(txt_code.getText());
            obj.setPhone_com(txt_phone_com.getText());
            obj.setStart_date_com(((JTextField) start_date.getDateEditor().getUiComponent()).getText());
            obj.setNotes(txta_notes.getText());
            obj.setName_com(txt_name_com.getText());
            obj.setEmail_com(txt_email_com.getText());
            obj.setNotes_com(txta_notes_com.getText());
            ob.insert(obj);
            
            txt_name.setText("");
            txt_address.setText("");
            txt_phone.setText("");
            txt_code.setText("");
            txt_phone_com.setText("");
            start_date.setDateFormatString("");
            txta_notes.setText("");
            txt_name_com.setText("");
            txt_email_com.setText("");
            txta_notes_com.setText("");
            
            vendors_cont.show_data();
        }
// TODO add your handling code here:
    }//GEN-LAST:event_add_buttonActionPerformed

